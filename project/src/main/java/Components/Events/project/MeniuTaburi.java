    private void jBtnVizFisiereActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnVizFisiereActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTableFisiere.getModel();
        fisiereSingle.clear();
        model.setRowCount(0);
        try {
            fisiereSingle = fisierSingleManagement.getAll();
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(MeniuTaburi.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        for (FisierSingle f : fisiereSingle) {
            model.addRow(new Object[]{f.getNume(), f.getTip(), f.getDescriere(), f.getLocatie()});
        }
    }//GEN-LAST:event_jBtnVizFisiereActionPerformed

